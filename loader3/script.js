const playButton = document.querySelector('.play-button');
const video = document.getElementById('video');
const timeline = document.querySelector('.timeline');
const soundButton = document.querySelector('.sound-button');
const fullscreenButton = document.querySelector('.fullscreen-button');
const videoContainer = document.querySelector('.video-player');
let isFullScreen = false;

const links = [];
links[0]= "../loader1/index.html";
links[1]= "../loader2/index.html";
links[2]= "../loader4/index.html";
links[3]= "../loader5/index.html";

function loadNew() {

let random_url = Math.floor(Math.random() * 4); 

 //console.log(random_url);
  location.href = links[random_url];
}

playButton.addEventListener('click', function () {
  if (video.paused) {
    //video.play();
    loadNew();
    videoContainer.classList.add('playing');
    playButton.innerHTML = pause;


  } else {
    //video.pause();
    videoContainer.classList.remove('playing');
    playButton.innerHTML = play;
    loadNew();
  }
})

video.onended = function () {
  playButton.innerHTML = play;
}

video.ontimeupdate = function () {
  const percentagePosition = (100*video.currentTime) / video.duration;
  timeline.style.backgroundSize = `${percentagePosition}% 100%`;
  timeline.value = percentagePosition;
}

timeline.addEventListener('change', function () {
  const time = (timeline.value * video.duration) / 100;
  video.currentTime = time;
});

soundButton.addEventListener('click', function () {
  video.muted = !video.muted;
  soundButton.innerHTML = video.muted ? mute : sound;
  loadNew();
});

fullscreenButton.addEventListener('click', function () {
  loadNew();
  if (!isFullScreen) {
    if (video.requestFullscreen) {
    video.requestFullscreen();
  } else if (video.webkitRequestFullscreen) { /* Safari */
    video.webkitRequestFullscreen();
  } else if (video.msRequestFullscreen) { /* IE11 */
    video.msRequestFullscreen();
  }
  } else {
    if (document.exitFullscreen) {
    document.exitFullscreen();
  } else if (document.webkitExitFullscreen) { /* Safari */
    document.webkitExitFullscreen();
  } else if (document.msExitFullscreen) { /* IE11 */
    document.msExitFullscreen();
  }
  }
});